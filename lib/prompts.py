REVIEW_ANALYSIS_SYSTEM = (
    "あなたはAmazon物販のレビュー分析担当です。競合商品の低評価レビューから、"
    "顧客の不満をテーマ別に正確に分類します。原文の転載はせず要約し、"
    "件数は与えられたリスト内の該当数の目安として扱い、憶測で数を作りません。"
)

def build_review_analysis_prompt(reviews: list[str]) -> tuple[str, str]:
    """(system, user) を返す。既存 HTML 版のプロンプトを忠実にポート。"""
    n = len(reviews)
    body = "\n".join(f"{i + 1}. {t}" for i, t in enumerate(reviews))
    user = (
        f"以下は競合商品の低評価（★1〜3中心）レビュー {n} 件です。\n"
        "顧客の不満をテーマ別に分類し、件数が多い順に、日本語のマークダウン箇条書きで簡潔にまとめてください。\n"
        "各テーマに「・不満テーマ：◯件目安 — 代表的な不満の要点（要約）／商品ページで訴求すべき改善方向」を1行で。\n"
        "最後に「最優先で訴求すべき不満トップ3」を箇条書きで添えてください。\n\n"
        "--- レビュー ---\n" + body
    )
    return REVIEW_ANALYSIS_SYSTEM, user


# ===== 勝ちパターン指針メモ（既存 app/index.html l.605-636 を忠実にポート）=====
# 構成案・コピー案プロンプトの system 先頭に置く商品非依存の stable prefix。
WIN_PATTERNS = """# 勝ちパターン指針メモ（訴求順序の骨格）

前提: Amazonの客はページを読み進めない（約7割スマホ）。上から順に「次を読みたい」と思わせ、離脱させない順序を組むことが訴求設計の本質。サブ画像は1枚ずつ独立して意味を持たせる。

## §1 6枚USP型（差別化を先頭に置く基本の骨格）— 心理の流れ 興味→信頼→共感→憧れ→納得→安心
1枚目 USP（最大の差別化）: 自社だけの最大の強みを一瞬で。抽象語でなくライバルが真似できない"事実"（具体数値・権威）。3秒で核心。
2枚目 客観的証拠・権威性: 第三者のお墨付き（専門家の推薦＋顔写真／検査機関の合格）で「自称の強み」への不信を壊す。曖昧な「安心」でなく具体データ。
3枚目 顧客ニーズ適合（悩み提起）: 競合の低評価レビュー由来の不満を「こんなお悩みありませんか？」で列挙→「○○が解決」＋ビフォーアフター図解。強い共感。
4枚目 理想の未来（ベネフィット）: スペック羅列でなく「So what?」で感情価値まで翻訳。使うシーンの人物写真で「使っている自分」を想像させる。
5枚目 比較表（ダメ押し）: ①競合の弱点(3枚目の不満を〇×) ②自社USP再掲 ③基本スペックの客観数値。客の比較検討を自社有利に代行。比較表現は根拠必須(§5)。
6枚目 クロージング（不安払拭）: カート直前の不安を先回りで全つぶし。同梱物全貌／詳細サイズ重量（返品防止）／保証・日本語サポート。

## §2 EPR法（悩み・共感ファースト）と型の使い分け
EPR = E(共感:まず悩み/理想を提示し「それ!」と掴む)→P(価値提案:顧客メリットで語る)→R(理由:数値/認証/専門家/顧客の声で裏づけ)。サブ1〜2枚目や説明文の冒頭に効く。
使い分け: 感情・共感が決め手の商品(悩み解決・コンプレックス・趣味性・ギフト・新規価値)=EPR型 / 論理的比較が重視される商品(PCパーツ・業務機器・汎用消耗品)=PREP寄り(結論=強みから)。

## §3 ファーストビュー(2枚目FV)に最低限入れる3要素
キャッチコピー(最重要:ベネフィットが伝わるか。競合が打ち出せない一言)／社会的証明＆権威性(早めに安心材料。ただしAmazon文言や証明困難な実績はNG)／代表的特徴3つ(絞って提示)。

## §5 法務・規約の注意（必ず通す）
・効果効能の断定（治る/改善/解消）=薬機法リスク、特に健康美容系は危険、避ける。
・「安心」「安全」=近年Amazonが優良誤認で問答無用削除しがち、なるべく避ける。
・Amazon文言（「Amazon売れ筋No.1」「Amazonおすすめ」等）=商標無断使用で避ける。
・無根拠の「No.1」「業界最高」「他社比で圧倒的」=景表法リスク、第三者調査データ無しなら避ける。
・証明困難な実績（自社調べ累計○個突破等）／「人生が変わる」等の感情的誇張=レビューが荒れる、避ける。
・原則: 迷ったら具体的・客観的な事実に置き換える。強い言葉より客観的事実の方が説得力が高い。

## §6 提案時の参照ルール
・商品性質で 6枚USP型 か EPR悩みファースト型 を選ぶ（感情共感型=EPR／論理比較型=PREP寄り）。
・3枚目(悩み提起)・5枚目(比較表)・EPRのE(共感)の"具体的な競合不満点"は商品ごとの入力欄「競合の不満点」から取る。件数の多い不満ほど優先訴求。
・「競合の不満点」が空のときは、その商品カテゴリの一般的な悩み・選定基準で一般論にフォールバックする（無理に具体不満をでっち上げない）。
・全案は §5 の法務チェックを通す。最終決定は常に人が行う（AIは複数案を出す係）。"""


def _product_get(product: dict, key: str) -> str:
    return str((product or {}).get(key, "") or "").strip()


def build_layout_prompt(product: dict, competitor_pains: str) -> tuple[str, str]:
    """③ 構成・レイアウト案の (system, user) を返す。app/index.html buildLayoutPrompt をポート。"""
    name      = _product_get(product, "productName")
    category  = _product_get(product, "category")
    color     = _product_get(product, "color")
    material  = _product_get(product, "material")
    size      = _product_get(product, "size")
    features  = _product_get(product, "features")
    target    = _product_get(product, "target")
    tone      = _product_get(product, "brandTone")
    ng        = _product_get(product, "ng")
    pains = (competitor_pains or "").strip()

    system = WIN_PATTERNS + """

---
あなたはAmazon物販の構成・レイアウト設計担当です。上の指針メモ（訴求順序の骨格）を土台に、与えられた商品情報の性質を判断して適切な型（USP型 または EPR悩みファースト型）を選び、各スライドの「役割＋パーツ配置のラフ」を構造テキストで提案します。

厳守事項:
- 実物商品や文字の清書はしない。画像・ASCIIワイヤー・PSDは描かない（テキストの構造案のみ）。
- §5の法務表現を必ず避ける（効果効能の断定・「安心」「安全」・Amazon文言・無根拠のNo.1/業界最高）。
- 最終決定は人が行う前提で、選びやすい複数案を出す。"""

    lines = ["# 商品情報", "商品名: " + (name or "(未入力)")]
    if category: lines.append("カテゴリ: " + category)
    if color:    lines.append("色: " + color)
    if material: lines.append("素材: " + material)
    if size:     lines.append("形状・サイズ: " + size)
    if features: lines.append("特徴:\n" + features)
    if target:   lines.append("ターゲット顧客: " + target)
    if tone:     lines.append("ブランドトーン: " + tone)
    if ng:       lines.append("NG要素（使わない表現・避けたい訴求）: " + ng)

    lines.append("\n# 競合の不満点（3枚目・比較表・EPRのE の根拠に使う）")
    if pains:
        lines.append(pains)
    else:
        lines.append("（入力なし）競合の不満点の入力が無いため、3枚目の悩み提起・5枚目の比較表・EPRのEは、このカテゴリの一般的な悩み／選定基準で一般論として構成してください（具体的な不満をでっち上げない）。")

    lines += [
        "\n# 出力指示",
        "上記を踏まえ、構成・レイアウト案を必ず3案、提案してください。各案は商品性質に応じて型（USP型 or EPR悩みファースト型）を選び、3案は型や切り口に変化を付けてください。",
        "各案は次の固定フォーマットで、案と案の間を「===案N===」の行で必ず区切ってください（パースに使います）:",
        "",
        "===案1===",
        "【雰囲気メモ】清潔・信頼感（このページ全体のトーン＆マナーを必ず1行で。「・」区切りで2語程度。例：清潔・信頼感／力強い・実用的）",
        "【型】USP型 または EPR悩みファースト型",
        "【採用理由】この商品にこの型が合う理由を1〜2行",
        "## スライド1：役割（例 USP/権威/悩み提起 等）",
        "- キャッチ文案: …",
        "- 入れる要素: …（箇条書き）",
        "- 配置メモ: …（どこに何を置くか短く）",
        "## スライド2：役割",
        "- （以下同様に、その型に応じた枚数ぶん）",
        "===案2===",
        "（同じ構造で別の型／切り口）",
        "===案3===",
        "（同じ構造でさらに別の切り口）",
    ]
    return system, "\n".join(lines)

        "【型】USP型 または EPR悩みファースト型",
        "【採用理由】この商品にこの型が合う理由を1〜2行",
        "## スライド1：役割（例 USP/権威/悩み提起 等）",
        "- 入れる要素: …（箇条書き）",
        "- 配置メモ: …（どこに何を置くか短く）",

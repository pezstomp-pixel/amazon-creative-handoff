        lines.append("（入力なし）競合の不満点の入力が無いため、3枚目の悩み提起・5枚目の比較表・EPRのE・共感コピーは、このカテゴリの一般的な悩み／選定基準で一般論として構成してください（具体的な不満をでっち上げない）。")

